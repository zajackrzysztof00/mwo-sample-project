import java.util.Date;

public class LongestPit {
    private final int driver;
    private final double time;

    public LongestPit(int driver, double time) {
        this.driver = driver;
        this.time = time;
    }

    public int getDriver() {
        return driver;
    }

    public double getDate() {
        return time;
    }
}
